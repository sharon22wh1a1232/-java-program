public class swap{
	public static void main(String[] args){
	int a=2;
	int b=3;
	System.out.println("before swaping");
	System.out.println("a="+a);
	System.out.println("b ="+b);
	int temp=a;
	a=b;
	b=temp;
	System.out.println("after swap");
	System.out.println("a="+a);
	System.out.println("b="+b);
	}
	}
	
